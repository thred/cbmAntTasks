package org.cbm.ant.cc65;

public class Library
{

    private String name;

    public Library()
    {
        super();
    }

    public Library(String name)
    {
        super();

        this.name = name;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

}
